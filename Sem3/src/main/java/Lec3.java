    public static int count(List <String> start, String element) {
        int counter= 0;

        for (String string : start) {
            if (element.equals(string)) counter++;
        }

        return counter;

    }

    // На входе список слов, на выходе map c парами слово - количество слов в списке
    public static Map<String,Integer> transformer(List <String> start) {
        Map<String,Integer> result = new HashMap<>(); // hashmap - это одно из воплощений Map наряду с TreeMap, и Hashtable
        for (String string : start){ // для каждого элемента типа string в массиве start
            // присвоить пару ключ-значение мэпу result. стринг будет ключом в паре, 
            //значением будет частота слова стринг в массиве старт
            result.put(string, Collections.frequency(start,string)); 
        }
        return result;
    }

